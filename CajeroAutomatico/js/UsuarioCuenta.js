class UsuarioCuenta{
    dni = "";
    nombres = "";
    apellidos = "";
    cuenta = "";
    monto = 0;
    moneda = "";
    constructor(dni, nombres, apellidos, cuenta, monto, moneda){
        this.dni = dni;
        this.nombres = nombres;
        this.apellidos = apellidos;
        this.cuenta = cuenta;
        this.monto = monto;
        this.moneda = moneda;
    }
}